public class Percent{

public static void main(String[] args){

int percent=35;

if(percent>90){

System.out.println("Given grade is A+");
}
else if(percent>75){

System.out.println("Given grade is A");
}
else if(percent>60){

System.out.println("Given grade is B");
}
else if(percent>40){

System.out.println("Given grade is C");
}
else{
System.out.println("Given grade is D");}
}
}